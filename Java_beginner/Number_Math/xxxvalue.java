package Java_beginner.Number_Math;

public class xxxvalue {
    public static void main(String[] args) {
        Integer n = 5;
        System.out.println(n.byteValue());
        // byteValue()方法是以byte形式返回指定的数值
        System.out.println(n.doubleValue());
        // doubleValue()方法是以double形式返回指定的数值
        System.out.println(n.longValue());
        // longValue()方法是以long形式返回指定的数值
        System.out.println(n.shortValue());
        // shortValue()方法是以short形式返回指定的数值
        System.out.println(n.floatValue());
        // floatValue()方法是以float形式返回指定的数值
    }
}


/*学习方法
类型                     方法及描述
byte                     byteValue()方法是以byte形式返回指定的数值
abstract double          doubleValue()方法是以double形式返回指定的数值
abstract float           floatValue()方法是以float形式返回指定的数值
abstract int             intValue()方法是以int形式返回指定的数值
abstract long            longValue()方法是以long形式返回指定的数值
short                    shortValue()方法是以short形式返回指定的数值
 */